--Explore for all counteries that the customer came from
select distinct country 
from gold.dim_customer

--Explore number of male \ female
select distinct(select count(*) from gold.dim_customer where gender='Female') as num_of_Female,
	   (select count(*) from gold.dim_customer where gender='Male') as num_of_male
from gold.dim_customer

--Explore the marital_status
select distinct marital_status from gold.dim_customer
select distinct 
		(select count(*) from gold.dim_customer where marital_status='Single') as num_of_singles,
	    (select count(*) from gold.dim_customer where marital_status='Married') as num_of_marrieds
from gold.dim_customer

--Explore how many men are married and how many women are married
select  
	gender , 
	count(case when marital_status = 'Single' then 1 end) as single,
	count(case when marital_status = 'Married' then 1 end) as married
from gold.dim_customer
where marital_status in ('Single', 'Married','n\a')
group by gender

--Explore the num of category and subcategroy 
select distinct count(category_id) from gold.dim_products

--Explore dates
select 
	min(order_date) as first_order_date,
	max(order_date) as last_order_date,
	datediff(MONTH,min(order_date), max(order_date)) as range_of_sales
from gold.fact_sales

--the oldest customer & youngest
select 
	min(birth_date) as youngest_cust,
	max(birth_date) as oldest_cust,
	datediff(year,min(birth_date),max(birth_date)) as diff_age
