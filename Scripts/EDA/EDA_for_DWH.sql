--find the number of customer by country 
select country , count(customer_key) as num_of_customer 
from gold.dim_customer
group by country 
order by country DESC

--find the total number of cutomer by gender 
select gender , count(customer_key) as num_of_customer
from gold.dim_customer
group by gender 
order by num_of_customer DESC

--find the total product by category 
select category , count(product_id) as total_products
from gold.dim_products 
group by category
order by total_products DESC

--find the average costs in each category 
select category , Round(avg(product_cost),2) as avg_cost
from gold.dim_products
group by category
order by avg_cost DESC

--find the total revenue generated from each category ?
select dp.category , sum(f.price) as total_revenue
from gold.Fact_Sales f
left join  gold.dim_products dp
	on f.product_id= dp.product_id
group by dp.category 
order by total_revenue DESC

--what is the total revenue generated by each customer?
select f.customer_id,cst.firstname,sum(f.sales_amount) as total_sales
from gold.dim_customer cst 
left join gold.Fact_Sales f 
	on cst.customer_id=f.customer_id
group by f.customer_id,cst.firstname
order by total_sales DESC

--what is the distribution of sold items across countries?
select cst.country, count(quantity) as total_items 
from gold.dim_customer cst
left join gold.Fact_Sales f
	on cst.customer_id=f.customer_id
group by cst.country 
order by total_items DESC

