--which products generated the highest revenue?
select top 5 p.product_name,sum(f.sales_amount) as total_revenue
from gold.dim_products p
left join gold.Fact_Sales f
	on f.product_id = p.product_id
group by f.product_id,p.product_name
order by total_revenue DESC


--using window functions
select * 
from(
	select  p.product_name,sum(f.sales_amount) as total_revenue,
	Rank()over(order by sum(f.sales_amount) DESC) as rnk
	from gold.dim_products p
	left join gold.Fact_Sales f
		on f.product_id = p.product_id
	group by f.product_id,p.product_name
) as sub
where rnk <=5
order by rnk 

---which customers generated the highest revenue?
select * 
from(
	select  cst.firstname,sum(f.sales_amount) as total_revenue,
	Rank()over(order by sum(f.sales_amount) DESC) as rnk
	from gold.dim_customer cst
	left join gold.Fact_Sales f
		on f.customer_id = cst.customer_id
	group by f.customer_id,cst.firstname
) as sub
where rnk <=5
order by rnk 


